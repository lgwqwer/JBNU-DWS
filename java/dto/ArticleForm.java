
public class ArticleForm {

    private Double grade = 0;
    private Double distScore = 0;

    public ArticleForm(Double grade, Double distScore) {
        this.grade = grade;
        this.distScore = distScore;
    }

    public Double getGrade() {
        return grade;
    }

    public Double getDistScore() {
        return distScore;
    }

}
